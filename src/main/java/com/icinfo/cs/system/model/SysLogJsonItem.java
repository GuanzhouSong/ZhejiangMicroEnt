package com.icinfo.cs.system.model;

/**
 * 描述:    日志前后变化<br>
 * 
 * @author 陈鑫
 * @date 2016年09月19日
 */
public class SysLogJsonItem {
	//字段名称
	private String itemKey;
	//变化前值
	private String beforeValue;
	//变化后值
	private String afterValue;
	public SysLogJsonItem(){}
	public SysLogJsonItem(String itemKey,String beforeValue,String afterValue){
		this.itemKey = itemKey;
		this.beforeValue = beforeValue;
		this.afterValue = afterValue;
	}
	public String getItemKey() {
		return itemKey;
	}
	public void setItemKey(String itemKey) {
		this.itemKey = itemKey;
	}
	public String getBeforeValue() {
		return beforeValue;
	}
	public void setBeforeValue(String beforeValue) {
		this.beforeValue = beforeValue;
	}
	public String getAfterValue() {
		return afterValue;
	}
	public void setAfterValue(String afterValue) {
		this.afterValue = afterValue;
	}
	@Override
	public String toString() {
		return "LogJsonItem [itemKey=" + itemKey + ", beforeValue="
				+ beforeValue + ", afterValue=" + afterValue + "]";
	}

}
